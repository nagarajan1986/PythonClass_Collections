#Arithmatic operators

a=100
b=900

print(a+b)   #it gives addition value
print(a-b)   #it gives subtract value
print(a*b)   #it gives multiply value
print(a/b)   #it gives divide value
print(a%b)   #it gives remainder value
print(a//b)   #it gives quotient value
print(a**b)   #it gives power of 2 value (i.e a power 2 and b power of 2)

#Relational operators / comparison operators
# it returns boolean value true or false only
#    < > >=  <= != ==

a=10
b=20

print(a>b)
print(a<b)
print(a>=b)
print(a<=b)
print(a!=b)
print(a==b)

#Logical operators
# and or not
# it returns boolean value true or false only

a=True
b=False
print(a and b) # false
print(a or b) #true
print(not a) #false

